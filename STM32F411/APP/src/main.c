/* ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝHAL๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอท๏ฟฝฤผ๏ฟฝ */
#include "stm32f4xx_hal.h"
/* ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝิถ๏ฟฝ๏ฟฝ๏ฟฝฯตอณอท๏ฟฝฤผ๏ฟฝ */
#include "sys.h"
/* ๏ฟฝๅผถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ */
#include "bsp_led.h"
#include "bsp_usart1.h"
/* FreeRTOS ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ */
#include "freertos_tasks.h"
/* TIM2 ๏ฟฝ฿พ๏ฟฝ๏ฟฝศผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ FreeRTOS ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฑอณ๏ฟฝ๏ฟฝสน๏ฟฝรฃ๏ฟฝ */
#include "bsp_timebase.h"

/**
  * @brief  ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ.
  * @retval int
  */
int main(void)
{
    /* 1. ๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝHAL๏ฟฝ๏ฟฝ
     *    ๏ฟฝฺฒ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ HAL_InitTick() ๏ฟฝ๏ฟฝ TIM5 ๏ฟฝ๏ฟฝฮช 1ms ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝSystTick ๏ฟฝ๏ฟฝ FreeRTOS๏ฟฝ๏ฟฝ */
    HAL_Init();

    /* 2. ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯตอณสฑ๏ฟฝ๏ฟฝ (HSE 25MHz ๏ฟฝ๏ฟฝ SYSCLK 100MHz, PCLK1 = 50MHz) */
    SystemClock_Config();

    /* 3. ๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ */
    BSP_LED_Init();

    /* 4. ๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ USART1 (DMA+๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝะถฯฃ๏ฟฝStreamBuffer) */
    BSP_USART1_Init();

    /* 5. ๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ TIM2 ๏ฟฝ฿พ๏ฟฝ๏ฟฝศผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ FreeRTOS ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฑอณ๏ฟฝฦฃ๏ฟฝ */
    BSP_TimBase_RunStats_Init();

    /* 6. ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ FreeRTOS ๏ฟฝฺบหฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝุฃ๏ฟฝ */
    freertos_start();

    /* ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝยฒ๏ฟฝ๏ฟฝ๏ฟฝึด๏ฟฝะต๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ */
    while (1) { }
}
